# The built-in print() function displays its argument onto the screen.

print(0)
print(-52)
print(1 + 17)
print(102 - 255)
print(38 * (-48))
print(5.2 / 3.1)
print(4 + 8j)
print("hello")
print(False)
print([5, 7, 0, -2, 23])

# Passing no arguments at all prints a blank line
print()

# Passing more than one argument displays each argument separated by a space
print("Goodbye", "world")