# The built-in print() function displays its argument onto the screen.

# There must not be a space between the function name and the opening parenthesis!
print(0)
print(-52)
print(1 + 17)
print(102 - 255)
print(38 * (-48))
print(5.2 / 3.1)
print(4 + 8j)
print("hello")
print(False)
print([5, 7, 0, -2, 23])

# One argument: displays its value and ends with a newline
print("Hello world!")

# When no argument is provided, displays no text and a newline
print()

# Displays Goodbye and world, separated by a single space, and a newline
print("Goodbye", "world")