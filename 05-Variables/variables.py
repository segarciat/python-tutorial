# Define variables
width = 8
height = 15

# Perform computations with variables
area = width * height
perimeter = (width * 2) + (height * 2)

# Display the value of variables
print(area)
print(perimeter)

# Reassign the value of existing variables
width = 0.7
height = 1.2

# Display the new variable values
print(width)
print(height)

# The area and perimeter variables used the old values, and their
# values are not affected (they are not "updated") by the reassignment of width and height.
print(area)
print(perimeter)

